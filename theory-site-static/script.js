$("copyEmailButton").addEventListener("click", async () => {
  try {
    await navigator.clipboard.writeText("barakbenhur@gmail.com");
    $("copyStatus").textContent = copy[lang].copiedText;
  } catch (error) {
    $("copyStatus").textContent = "barakbenhur@gmail.com";
  }
});
